package Syntax;

public class Syntax {
    public static void main (String[] args) {
        int a=3;
        int b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/b);
        System.out.println(b%a);
        System.out.println(++b);
        System.out.println();
        a=3;
        b=5;
        System.out.println(a+=b);
        a=3;
        b=5;
        System.out.println(a-=b);
        a=3;
        b=5;
        System.out.println(a*=b);
        a=3;
        b=5;
        System.out.println(b/=a);
        a=3;
        b=5;
        System.out.println(b%=a);
        a=3;
        b=5;
        System.out.println(--b);
    }
}
